package AllLessons.Lesson11;

public class Asterisks {
    public static void main(String[] args) {
        int n = 5;
        for (int i = 0; n > i; i++) {
            for (int k = 0; n > k; k++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
