package AllLessons.Lesson7;

public class Menu {
    public void startcalc() {
        System.out.println("Калькулятор включен, введите число");
    }

    public void opcalc() {
        System.out.println("Введите один из четырех операндов +, -, * , / ");
    }

    public void rescalc() {
        System.out.println("Введите второе число и нажмите Enter");
    }

    public void endcalc() {
        System.out.println("Чтобы завершить работу нажмите e");
    }

    public void offcalc() {
        System.out.println("Калькулятор выключен");
    }
}
